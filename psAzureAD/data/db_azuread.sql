-- Create PS_AZUREAD_CONFIGURATIONS
CREATE TABLE IF NOT EXISTS `PS_AZUREAD_CONFIGURATIONS` (
    `PAADC_ID` int(11) NOT NULL AUTO_INCREMENT,
    `TENANT_ID` varchar(50) NULL DEFAULT '',
    `CLIENT_ID` varchar(50) NULL DEFAULT '',
    `CLIENT_SECRET` varchar(50) NULL DEFAULT '',
    `APP_ACCESS_TOKEN` mediumtext NULL DEFAULT '',
    PRIMARY KEY (`PAADC_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;

